    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int cuenta,i=0,posmayor=-1,posmenor=-1;
        double may=0,men=500,p;
        String pmay,pmen;
        cuenta = modelo4.getSize();
        if(cuenta>=1){
            for(i=0;i<cuenta;i++){
                p=Double.parseDouble((String)modelo4.getElementAt(i));
                if(p>may){
                    may=p;
                    posmayor=i;
                }
                if(p<men){
                    men=p;
                    posmenor=i;
                }
            }
            setMayor(may);
            setMenor(men);
            pmay=(String)modelo1.elementAt(posmayor);
            pmen=(String)modelo1.elementAt(posmenor);
            setPostMay(pmay);
            setPostMen(pmen);
            frmEjercicio7_1 form2 = new frmEjercicio7_1();
            form2.mayor=getMayor();
            form2.menor=getMenor();
            form2.postMay=getPostMay();
            form2.postMen=getPostMen();
            form2.show();;
            
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

