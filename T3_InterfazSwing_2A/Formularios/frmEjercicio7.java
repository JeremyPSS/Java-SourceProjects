    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

//        double puntaje;
        int buenas = Integer.parseInt(txtBien.getText());
        int malas = Integer.parseInt(txtMal.getText());
//        puntaje = (double) buenas*4.8 - (double)malas*1.04;
        String nombre  = txtPost.getText();
        Postulante_e7 op = new Postulante_e7(nombre, buenas, malas);
        
        if((buenas+malas)<=100){
            modelo1.addElement(op.getNombre());
            modelo2.addElement(String.valueOf(op.getPbien()));
            modelo3.addElement(String.valueOf(op.getPmal()));
            modelo4.addElement(String.valueOf(op.Puntaje()));
            jButton2.setEnabled(false);
            
        }else{
            JOptionPane.showMessageDialog(null, "Las preguntas bien y mal constadas no debe superar los 100 puntos");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

