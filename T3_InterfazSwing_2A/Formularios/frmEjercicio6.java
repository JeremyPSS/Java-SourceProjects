    public void Eliminar(){
        int fila,total;
        fila=jTable1.getSelectedRow();
        if(fila>=0){
            dtm.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(null, "Selecciona una fila de la Tabla para elegir");
        }
        total=dtm.getRowCount();
        txtTotal.setText(String.valueOf(total));
       
    }
    
