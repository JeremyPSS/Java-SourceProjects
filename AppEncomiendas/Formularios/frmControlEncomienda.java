    private void ListaDatos(){
        jComboBox1.addItem("<Seleccionar Guia>");
        for (int i = 0; i < TListaGuiaEnvio.Count(); i++) {
            GuiaEnvio op = TListaGuiaEnvio.obtenerGuiaEnvio(i);
            if(op.getEstado().equals("ENVIADO")){
                jComboBox1.addItem(op.getCodigo());
            }
        }
    }

