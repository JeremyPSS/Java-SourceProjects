    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedIndex() > 0){
            
            String Codigo = jComboBox1.getSelectedItem().toString();
            
            int pos = TListaGuiaEnvio.BuscarCodigo(Codigo);
            GuiaEnvio oe = TListaGuiaEnvio.obtenerGuiaEnvio(pos);
            
            int posCliente = TListaCliente.BuscarCodigo(oe.getCodCliente());
            Clientes oc = TListaCliente.obtenerCliente(posCliente);
            
            jTextField1.setText(oc.getNombres());
            
            int posCamion = TListaCamion.BuscarCamionId(oe.getIdCamion());
            Camiones op = TListaCamion.obtenerCamion(posCamion);
            
            jTextField2.setText(op.getPlaca());
            
            jTable1.setModel(TListaGuiaEnvio.tablaModeloEncomienda(Codigo));
            
        }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

