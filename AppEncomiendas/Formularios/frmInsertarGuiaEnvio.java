    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // TODO add your handling code here:
        if(jComboBox2.getSelectedIndex() > 0){
            int pos = TListaCamion.BuscarCamionPlaca(jComboBox2.getSelectedItem().toString());
            Camiones ob = TListaCamion.obtenerCamion(pos);
            int idCamion        = ob.getId();
            String placa        = ob.getPlaca();
            double pesoMaximo   = ob.getPeso();
            double pesoOcupado  = TListaGuiaEnvio.PesoOcupado(idCamion);
            double pesoDisponible = pesoMaximo - pesoOcupado;

            jTextField2.setText(String.valueOf(pesoMaximo));
            jTextField3.setText(String.valueOf(pesoDisponible));
            
            if(pesoDisponible == 0){
                btnGuardar.setEnabled(false);
            }else{
                btnGuardar.setEnabled(true);
            }
            
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

