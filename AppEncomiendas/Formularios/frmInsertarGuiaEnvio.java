    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("") && jComboBox1.getSelectedIndex() > 0 && jComboBox2.getSelectedIndex() > 0){

            try {

                String Codigo       = jTextField1.getText();

                String Nombres      = jComboBox1.getSelectedItem().toString();
                int posCliente      = TListaCliente.BuscarNombre(Nombres);
                Clientes objCliente = TListaCliente.obtenerCliente(posCliente);
                String CodCliente   = objCliente.getCodigo();
                
                String Placa        = jComboBox2.getSelectedItem().toString();
                int posCamion       = TListaCamion.BuscarCamionPlaca(Placa);
                Camiones objCamion  = TListaCamion.obtenerCamion(posCamion);
                int IdCamion        = objCamion.getId();
                
                GuiaEnvio op = new GuiaEnvio(Codigo, CodCliente, IdCamion,"ENVIADO");
                

                if(funcion.equals("Insertar")){
                    
                    TListaGuiaEnvio.Agregar(op);
                    TListaGuiaEnvio.ActualizarEstadoEncomienda(Codigo, "ENVIADO");
                    TListaGuiaEnvio.AtualizarEstadoCamion(IdCamion, "OCUPADO");
                    
                }else{
                    TListaGuiaEnvio.Actualizar(op, TListaEncomienda.BuscarEncomienda(Codigo));
                }

                frmAdminGuiaEnvio.ListarDatos();
                
                TListaGuiaEnvio.LimpiarEncomiendaTemp();
                
                this.dispose();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        }else{
            JOptionPane.showMessageDialog(null, "Ingrese correctamente los datos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

