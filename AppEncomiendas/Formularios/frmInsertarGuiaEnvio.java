    private void ListarDatos(){
        
        jComboBox1.addItem("<Seleccione Cliente>");
        for (int i = 0; i < TListaCliente.Count(); i++) {
            Clientes ob = TListaCliente.obtenerCliente(i);
            
            int pos = TListaGuiaEnvio.BuscarCliente(ob.getCodigo());
            
            if(pos == -1){
                jComboBox1.addItem(ob.getNombres());
            }
            
        }
        
        jComboBox2.addItem("<Seleccione Placa>");
        for (int i = 0; i < TListaCamion.Count(); i++) {
            Camiones ob = TListaCamion.obtenerCamion(i);
            if(ob.getEstado().equals("DISPONIBLE")){
                jComboBox2.addItem(ob.getPlaca());
            }
        }
        
        jTable1.setModel(TListaGuiaEnvio.tablaModelEncomiendaTemp());
        
    }

    public static void setDatos(Encomiendas oe){
        double peso = Double.parseDouble(jTextField3.getText());
        if (oe.getPeso() <= peso){
            
            TListaGuiaEnvio.AgregaEncomiendaTemp(oe);
            jTable1.setModel(TListaGuiaEnvio.tablaModelEncomiendaTemp());
            
        }else{
            JOptionPane.showMessageDialog(null, "El peso de la encomienda supera al peso maximo del camion");
        }
        
    }
    
