    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:

        if(jTable1.getSelectedRow() >= 0){
            //COLUMNA
            //0 - CODIGO
            
            int fila    = jTable1.getSelectedRow();
            int columna = 0;

            String buscar = jTable1.getValueAt(fila, columna).toString();
            int pos = TListaEncomienda.BuscarEncomienda(buscar);

            Encomiendas op = TListaEncomienda.obtenerEncomienda(pos);

            frmInsertarEncomienda frmVentana = new frmInsertarEncomienda("Modificar");
            frmVentana.setDatos(op);
            frmVentana.setVisible(true);

        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow() >= 0){
            int op = JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar el registro seleccionado","Eliminar",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
            if(op == JOptionPane.YES_OPTION){

                //COLUMNA
                //0 - CODIGO
                
                int fila    = jTable1.getSelectedRow();
                int columna = 0;

                String buscar = jTable1.getValueAt(fila, columna).toString();
                int pos = TListaEncomienda.BuscarEncomienda(buscar);

                TListaEncomienda.Borrar(pos);

                frmAdminEncomiendas.ListarDatos();
            }

        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un encomienda");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

